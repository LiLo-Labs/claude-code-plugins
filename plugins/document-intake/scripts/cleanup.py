#!/usr/bin/env python3
"""Stage 3 — Deterministic cleanup. Document-agnostic.

Applies generic rules (no hardcoded vendor/format strings):
  • Strip page headers/footers/copyright/timestamps that repeat verbatim
    across ≥3 `## Page N` sections (auto-detected).
  • Drop decorative-image tags, bare page numbers, page counters.
  • Heuristic field-label folding: short TitleCase label + value on next line
    becomes `- **Label:** value`.
  • OneTrust-style Q/A detection: `N.M\\n<question>\\nResponse\\n<answer>`
    becomes `**Q N.M** — <question>  **A:** <answer>`.
  • Risk records (Inherent risk score / Residual / Risk description / … / Date
    closed) become `**RISK**` records with bulleted fields.
  • PDF column-wrap line breaks joined when a line doesn't end with terminal
    punctuation.

Operates IN PLACE on `.md` files in the processed folder and writes
`_summary.md` + `_summary.json` alongside. No intermediate directory.

Usage:
    python3 cleanup.py --dir <processed-folder>
"""
from __future__ import annotations
import argparse, collections, json, pathlib, re

DROP_WHOLE_LINE = [
    (re.compile(r"^\s*\*\[decorative icon.*?\]\*\s*$"),                         "decorative-image"),
    (re.compile(r"^\s*\*\[small graphic.*?low informational value.*?\]\*\s*$"), "low-value-image"),
    (re.compile(r"^\s*\*\[graphic — \d+KB @.*?not viewed.*?\]\*\s*$"),          "unviewed-image"),
    (re.compile(r"^\s*\*\[Image — .*?Technology stack logo.*?icon\.\]\*\s*$"),  "logo-icon"),
    (re.compile(r"^\s*Page\s+\d+\s+of\s+\d+\s*$", re.IGNORECASE),               "page-counter"),
    (re.compile(r"^\s*\d+\s*/\s*\d+\s*$"),                                      "page-counter"),
    # Do NOT drop bare numbers — they're often answer values like "50" for
    # "Less than 50". Running-header detection catches genuine page-number
    # noise when it repeats on many pages. Keep explicit page-counter patterns
    # above (`N / M`, `Page N of M`) — those are unambiguous.
    (re.compile(r"^##\s*Page\s+\d+\s*$"),                                       "page-break"),
    (re.compile(r"^\s*©\s*\d{4}\b.*?All rights reserved\.?\s*$", re.IGNORECASE),"copyright"),
    (re.compile(r"^\s*\d{4}-\d{2}-\d{2}\s+\d{2}:\d{2}:\d{2}(\s+[\w\s]+)?\s*$"), "timestamp-footer"),
]

_STRUCTURAL_ALLOWLIST = {
    "Response", "Explanation", "Risks", "Risk", "Answer", "Question", "Notes",
    "Comments", "Description", "Response:", "Explanation:",
    "Risk category", "Risk description", "Risk owners", "Risk approver",
    "Risk deadline", "Stage", "Treatment plan", "Result", "Date closed",
    "Inherent risk score", "Residual risk score",
}

LABEL_RX = re.compile(r"^[A-Z][A-Za-z][A-Za-z\s&/()\-]{1,25}:?$")
def IS_LABEL_LIKE(s: str) -> bool:
    s = s.strip()
    if not LABEL_RX.match(s): return False
    words = s.rstrip(":").split()
    if len(words) > 2: return False
    if any(ch.isdigit() for ch in s): return False
    return all(w[:1].isupper() for w in words)

QNUM = re.compile(r"^\d+(?:\.\d+)+\s*$")
QSECTION = re.compile(r"^\d+\s*$")
BOILERPLATE = re.compile(r"\*\[boilerplate auto-risk block removed[^\]]*\]\*")


def detect_running_lines(text: str) -> set[str]:
    pages = re.split(r"(?m)^##\s*Page\s+\d+\s*$", text)
    counts: collections.Counter = collections.Counter()
    for p in pages:
        seen = set()
        for ln in p.splitlines():
            s = ln.strip()
            if s in _STRUCTURAL_ALLOWLIST: continue
            looks = (len(s) >= 20 or bool(re.search(
                r"(©|Page\s+\d+\s+of\s+\d+|Confidential|Last Updated|Generated by)", s)))
            if not looks: continue
            if 4 < len(s) < 200 and s not in seen:
                seen.add(s); counts[s] += 1
    threshold = max(3, len(pages) // 2)
    return {ln for ln, c in counts.items() if c >= threshold}


def filter_lines(text, running, audit):
    out = []
    for ln in text.splitlines():
        matched = False
        for rx, reason in DROP_WHOLE_LINE:
            if rx.match(ln):
                audit[f"dropped:{reason}"] += 1
                matched = True; break
        if matched: continue
        if ln.strip() in running:
            audit["dropped:running-header"] += 1; continue
        out.append(ln.rstrip())
    return out


def collapse_boilerplate(lines, audit):
    out, i = [], 0
    while i < len(lines):
        if BOILERPLATE.search(lines[i]):
            count = 0
            while i < len(lines) and (BOILERPLATE.search(lines[i]) or lines[i].strip() == ""):
                if BOILERPLATE.search(lines[i]): count += 1
                i += 1
            out.append(f"*[boilerplate zero-risk blocks removed: ×{count} auto-accepted]*")
            out.append("")
        else:
            out.append(lines[i]); i += 1
    return out


def structure_risks(text, audit):
    block_re = re.compile(
        r"(?P<inherent>Inherent risk score\s*\n\s*[^\n]+)\n"
        r"(?:(?P<residual>Residual risk score\s*\n\s*[^\n]+)\n)?"
        r"(?:Risk\s*\n\s*description\s*\n(?P<desc1>(?:(?!Risk category).)+?)"
        r"|Risk description\s+(?P<desc2>[^\n]+)\n)"
        r"Risk category\s*\n\s*(?P<cat>[^\n]+)\n"
        r"Stage\s*\n\s*(?P<stage>[^\n]+)\n"
        r"(?:Risk approver\s*\n\s*(?P<approver>[^\n]+)\n)?"
        r"(?:Risk owners\s*\n\s*(?P<owners>[^\n]+)\n)?"
        r"(?:Risk deadline\s*\n\s*(?P<deadline>[^\n]+)\n)?"
        r"Treatment plan\s*\n(?P<plan>(?:(?!(?:Result|Treatment\s+[A-Z])\s*\n).)+?)"
        r"(?:Treatment\s+(?P<tid>[A-Z\-0-9]+)\s*\n)?"
        r"Result\s*\n\s*(?P<result>[^\n]+)\n"
        r"Date closed\s*\n\s*(?P<closed>[^\n]+)",
        re.DOTALL,
    )
    def fmt(m):
        audit["structured:risk-records"] += 1
        g = lambda n, d="—": (m.group(n).strip() if m.group(n) else d)
        inh = m.group("inherent").replace("\n"," ").replace("Inherent risk score","").strip()
        res = ""
        if m.group("residual"):
            r = m.group("residual").replace("\n"," ").replace("Residual risk score","").strip()
            res = f" · **Residual:** {r}"
        desc = " ".join((m.group("desc1") or m.group("desc2") or "").split())
        plan = " ".join(m.group("plan").split())
        extras = []
        if m.group("approver"): extras.append(f"**Approver:** {g('approver')}")
        if m.group("owners"):   extras.append(f"**Owners:** {g('owners')}")
        if m.group("deadline"): extras.append(f"**Deadline:** {g('deadline')}")
        ex = " · ".join(extras)
        tid = f" (Treatment {m.group('tid')})" if m.group("tid") else ""
        return (
            f"\n**RISK** (Inherent: {inh}{res})  \n"
            f"- **Description:** {desc}  \n"
            f"- **Category:** {g('cat')} · **Stage:** {g('stage')}" + (f" · {ex}" if ex else "") + "  \n"
            f"- **Treatment plan:** {plan}  \n"
            f"- **Result:** {g('result')}{tid} · **Closed:** {g('closed')}\n"
        )
    return block_re.sub(fmt, text)


def parse_chunk(chunk):
    sections = [("question", [])]
    for ln in chunk:
        s = ln.strip()
        if s == "Response":      sections.append(("response", []))
        elif s == "Explanation": sections.append(("explanation", []))
        elif s == "Risks":       sections.append(("risks", []))
        else:                    sections[-1][1].append(ln)
    question, responses, explanations, risk_lines = "", [], [], None
    for label, content in sections:
        text = " ".join(x.strip() for x in content if x.strip()).strip()
        if   label == "question":     question = text
        elif label == "response":     responses.append(text)
        elif label == "explanation":  explanations.append(text)
        elif label == "risks":        risk_lines = [x for x in content if x.strip()]
    return question, responses, explanations, risk_lines


def pick_answer(responses):
    # Prefer the LAST non-empty response; if multiple, the second instance is
    # usually the real answer (first was an echoed context).
    last_nonempty = [r for r in responses if r]
    return last_nonempty[-1] if last_nonempty else "(not answered)"

def pick_explanation(explanations):
    for e in explanations[::-1]:
        if not e: continue
        low = e.lower().strip()
        if low == "none": continue
        if low.startswith("none ") and len(e) < 60: continue
        if low.startswith("none "):
            return e[5:].strip()
        return e
    return None


def onetrust_qa(lines, audit):
    out, i, n = [], 0, len(lines)
    while i < n:
        ln = lines[i].strip()
        if QNUM.match(ln):
            qnum = ln
            j = i + 1
            chunk = []
            while j < n and not QNUM.match(lines[j].strip()):
                chunk.append(lines[j]); j += 1
            q, r, e, rl = parse_chunk(chunk)
            a = pick_answer(r)
            exp = pick_explanation(e)
            if q: out.append(f"**Q {qnum}** — {q}")
            else: out.append(f"**Q {qnum}**")
            out.append(f"**A:** {a}")
            if exp: out.append(f"**Explanation:** {exp}")
            if rl: out.append(""); out.extend(rl)
            out.append("")
            audit["structured:qa-pairs"] += 1
            i = j; continue
        if QSECTION.match(ln) and i + 1 < n and lines[i+1].strip() and not QNUM.match(lines[i+1].strip()):
            title = lines[i+1].strip()
            if len(title) < 120 and not title.endswith("."):
                out.append(f"### {ln}. {title}"); i += 2; continue
        out.append(lines[i]); i += 1
    return out


def heuristic_field_labels(lines, audit):
    out, i = [], 0
    while i < len(lines):
        s = lines[i].strip()
        if IS_LABEL_LIKE(s) and i + 1 < len(lines):
            nxt = lines[i+1]
            if nxt.strip() == "": out.append(lines[i]); i += 1; continue
            v = nxt.strip()
            if IS_LABEL_LIKE(v) or QNUM.match(v) or v.startswith(("**","- ","#","*[")):
                out.append(lines[i]); i += 1; continue
            out.append(f"- **{s.rstrip(':')}:** {v}")
            audit["structured:label-value-pairs"] += 1
            i += 2; continue
        out.append(lines[i]); i += 1
    return out


ORPHAN = re.compile(r"^(Response|Explanation)\s+")
def drop_orphans(lines, audit):
    out = []
    for ln in lines:
        s = ln.strip()
        if s in ("Response", "Explanation"):
            audit["dropped:orphan-label"] += 1; continue
        if ORPHAN.match(s):
            content = ORPHAN.sub("", s)
            if content.lower().strip() == "none":
                audit["dropped:orphan-label"] += 1; continue
            out.append(f"- **{s.split()[0]}:** {content}")
            continue
        out.append(ln)
    return out


SENT_END = re.compile(r"[.!?:;)\]\"]$")
STRUCT_START = re.compile(r"^(#|\*\*|- |\*\[|> |Q \d|A: |RISK )")
def join_wrapped_lines(lines, audit):
    out, buf, joins = [], "", 0
    for ln in lines:
        s = ln.rstrip()
        if s == "":
            if buf: out.append(buf); buf = ""
            out.append(""); continue
        if STRUCT_START.match(s) or s.startswith("#"):
            if buf: out.append(buf)
            buf = s; continue
        if not buf: buf = s; continue
        if not SENT_END.search(buf) and len(buf) < 140:
            buf += " " + s; joins += 1
        else:
            out.append(buf); buf = s
    if buf: out.append(buf)
    audit["joined:wrapped-lines"] = joins
    return out


def normalize_blanks(lines):
    out, prev = [], False
    for ln in lines:
        if ln.strip() == "":
            if prev: continue
            prev = True; out.append("")
        else:
            prev = False; out.append(ln)
    while out and out[-1].strip() == "": out.pop()
    return out


def process(path):
    raw = path.read_text()
    audit: collections.Counter = collections.Counter()
    running = detect_running_lines(raw)
    if running: audit["detected:running-headers"] = len(running)
    text = structure_risks(raw, audit)
    lines = filter_lines(text, running, audit)
    lines = collapse_boilerplate(lines, audit)
    lines = onetrust_qa(lines, audit)
    lines = heuristic_field_labels(lines, audit)
    lines = drop_orphans(lines, audit)
    lines = join_wrapped_lines(lines, audit)
    lines = normalize_blanks(lines)
    return "\n".join(lines) + "\n", dict(audit), running


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--dir", required=True, help="Processed folder")
    args = ap.parse_args()

    root = pathlib.Path(args.dir).expanduser().resolve()
    (root / "_polish_reports").mkdir(exist_ok=True)
    snapshot_dir = root / ".snapshot"; snapshot_dir.mkdir(exist_ok=True)

    aggregate = {}
    for md in sorted(root.glob("*.md")):
        if md.name.startswith("_"): continue    # skip _summary.md, etc.
        orig_lines = len(md.read_text().splitlines())
        cleaned, audit, running = process(md)
        md.write_text(cleaned)                  # overwrite in place
        # Save a snapshot of the post-cleanup / pre-polish state so the verify
        # stage can diff against the final polished output.
        (snapshot_dir / md.name).write_text(cleaned)
        aggregate[md.name] = {
            "orig_lines": orig_lines,
            "new_lines":  len(cleaned.splitlines()),
            "audit":      audit,
            "running_headers_stripped": sorted(running),
        }
        print(f"{md.name}: {orig_lines} → {aggregate[md.name]['new_lines']}")

    # Summary markdown.
    lines = ["# Cleanup Summary — `manipulated/`", "",
             "Deterministic pass.",
             "",
             "## What this pass did",
             "",
             "- Dropped decorative-image placeholders, running headers/footers, page counters, copyrights.",
             "- Collapsed blank-value metadata fields.",
             "- Detected and structured questionnaire Q/A pairs (`N.M` → `**Q**/**A:**`).",
             "- Reformatted risk blocks into bulleted records.",
             "- Joined PDF column-wrap line breaks.",
             "- Auto-detected running headers by verbatim repetition across `## Page N` sections.",
             "",
             "## What this pass did NOT do",
             "",
             "- No semantic judgments about prose relevance.",
             "- No table reconstruction (LLM polish does this).",
             "- No cross-page logical joining.",
             "",
             "## Per-file audit",
             "",
             "| File | Lines | Dropped noise | QA pairs | Risk records | Field bullets | Joined wraps |",
             "|------|-------|---------------|----------|--------------|----------------|---------------|"]
    for name, info in sorted(aggregate.items()):
        a = info["audit"]
        decor = sum(a.get(f"dropped:{k}", 0) for k in
                    ("decorative-image","low-value-image","unviewed-image","logo-icon",
                     "running-header","page-counter","page-break","bare-number",
                     "copyright","timestamp-footer"))
        lines.append(
            f"| {name} | {info['orig_lines']}→{info['new_lines']} | {decor} | "
            f"{a.get('structured:qa-pairs',0)} | {a.get('structured:risk-records',0)} | "
            f"{a.get('structured:label-value-pairs',0)} | {a.get('joined:wrapped-lines',0)} |"
        )
    lines.extend(["", "## Aggregate", ""])
    to = sum(i["orig_lines"] for i in aggregate.values())
    tn = sum(i["new_lines"]  for i in aggregate.values())
    lines.append(f"- Total lines: **{to} → {tn}** ({100*tn/to:.0f}% of original)")
    lines.append(f"- Files processed: **{len(aggregate)}**")
    (root / "_summary.md").write_text("\n".join(lines) + "\n")
    (root / "_summary.json").write_text(json.dumps(aggregate, indent=2))


if __name__ == "__main__":
    main()
